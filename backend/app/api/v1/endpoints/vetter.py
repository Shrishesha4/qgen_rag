@router.post("/questions/{question_id}/vet")
async def vet_question(
    question_id: uuid.UUID,
    vet_data: VetQuestionRequest,
    current_user: User = Depends(get_current_vetter),
    db: AsyncSession = Depends(get_db),
):
    """
    Approve or reject a question.
    """
    # Get question
    result = await db.execute(
        select(Question).where(
            Question.id == question_id,
            Question.is_latest == True,
            Question.is_archived == False,
        )
    )
    question = result.scalar_one_or_none()
    
    if not question:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Question not found",
        )
    
    # Update vetting status
    question.vetting_status = vet_data.status
    question.vetting_notes = vet_data.notes
    question.vetted_at = datetime.now(timezone.utc)
    question.vetted_by = current_user.id
    
    # Update CO mapping if provided
    if vet_data.course_outcome_mapping:
        question.course_outcome_mapping = vet_data.course_outcome_mapping
    
    # Store rejection reasons if rejected
    if vet_data.status == "rejected" and vet_data.rejection_reasons:
        question.vetting_notes = f"{vet_data.notes or ''}\nReasons: {', '.join(vet_data.rejection_reasons)}"
        if vet_data.custom_feedback:
            question.vetting_notes += f"\nFeedback: {vet_data.custom_feedback}"
    
    await db.commit()
    
    return {
        "message": f"Question {vet_data.status}",
        "question_id": str(question_id),
        "status": vet_data.status,
    }


