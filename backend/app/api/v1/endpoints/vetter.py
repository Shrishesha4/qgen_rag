@router.get("/dashboard", response_model=VetterDashboard)
async def get_vetter_dashboard(
    current_user: User = Depends(get_current_vetter),
    db: AsyncSession = Depends(get_db),
):
    """
    Get vetter dashboard with aggregate stats across all teachers.
    """
    # Count pending questions
    pending_result = await db.execute(
        select(func.count(Question.id)).where(
            Question.vetting_status == "pending",
            Question.is_latest == True,
            Question.is_archived == False,
        )
    )
    total_pending = pending_result.scalar() or 0
    
    # Count approved questions
    approved_result = await db.execute(
        select(func.count(Question.id)).where(
            Question.vetting_status == "approved",
            Question.is_latest == True,
            Question.is_archived == False,
        )
    )
    total_approved = approved_result.scalar() or 0
    
    # Count rejected questions
    rejected_result = await db.execute(
        select(func.count(Question.id)).where(
            Question.vetting_status == "rejected",
            Question.is_latest == True,
            Question.is_archived == False,
        )
    )
    total_rejected = rejected_result.scalar() or 0
    
    # Count teachers with pending questions
    teachers_result = await db.execute(
        select(func.count(func.distinct(Subject.user_id))).where(
            Subject.id.in_(
                select(Question.subject_id).where(
                    Question.vetting_status == "pending",
                    Question.is_latest == True,
                    Question.is_archived == False,
                    Question.subject_id.isnot(None),
                )
            )
        )
    )
    teachers_with_pending = teachers_result.scalar() or 0
    
    # Count subjects with pending questions
    subjects_result = await db.execute(
        select(func.count(func.distinct(Question.subject_id))).where(
            Question.vetting_status == "pending",
            Question.is_latest == True,
            Question.is_archived == False,
            Question.subject_id.isnot(None),
        )
    )
    subjects_with_pending = subjects_result.scalar() or 0
    
    # Count recent submissions (last 24 hours)
    yesterday = datetime.now(timezone.utc).replace(hour=0, minute=0, second=0, microsecond=0)
    recent_result = await db.execute(
        select(func.count(Question.id)).where(
            Question.generated_at >= yesterday,
            Question.is_latest == True,
            Question.is_archived == False,
        )
    )
    recent_submissions = recent_result.scalar() or 0
    
    return VetterDashboard(
        total_pending=total_pending,
        total_approved=total_approved,
        total_rejected=total_rejected,
        teachers_with_pending=teachers_with_pending,
        subjects_with_pending=subjects_with_pending,
        recent_submissions=recent_submissions,
    )


